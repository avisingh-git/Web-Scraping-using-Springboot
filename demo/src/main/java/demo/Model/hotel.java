package demo.Model;

public class hotel {
    
    String name;
    String wifi;
    String description;
    String roomType;
    String bedType;

    public hotel(String name, String wifi, String description, String roomType, String bedType) {
        this.name = name;
        this.wifi = wifi;
        this.description = description;
        this.roomType = roomType;
        this.bedType = bedType;
    }

}
